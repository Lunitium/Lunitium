using System.Text;
using Lunitium.Mapper.Generator.Enums;
using Lunitium.Mapper.Generator.Models;
using Microsoft.CodeAnalysis;

namespace Lunitium.Mapper.Generator.Templates;

public class MapperTemplate(SourceProductionContext context, MapperInfos mapper)
{
    public string Render()
    {
        var sb = new StringBuilder(2048);

        sb.AppendLine("// <auto-generated />");
        sb.AppendLine();
        sb.AppendLine($"namespace {mapper.Namespace};");
        sb.AppendLine();
        sb.AppendLine($"public partial class {mapper.ModelName}");
        sb.AppendLine("{");

        foreach (var mapperInfo in mapper.Infos)
        {
            var template = new MapperToTemplate(context, mapperInfo);

            if (mapperInfo.MapDirection.HasFlag(MapDirection.To))
            {
                sb.AppendLine(template.Render());
            }
        }

        sb.AppendLine("}");

        return sb.ToString();
    }
}